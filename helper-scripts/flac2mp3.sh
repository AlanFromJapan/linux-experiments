#!/bin/bash
#---------------------------------
# Converts a full folder of .flac to .mp3.
# Requires ffmpeg
#

for f in "$1"/*.flac; do
	#files only
	if [ -f "$f" ]; then
		ffmpeg -i "$f" -q:a 0 -map a "${f[@]/%flac/mp3}"
		echo "==================================================================================="
		echo "==================================================================================="
		echo "==================================================================================="
	fi
done

